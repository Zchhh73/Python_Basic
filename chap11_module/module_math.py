# math模块
import math
import random

# 舎入函数
print(math.ceil(1.4))
print(math.floor(1.4))
print(round(1.4))

# 幂和对数函数
print(math.log(8, 2))
print(math.pow(2, 3))
print(math.log(8))
print(math.sqrt(4))

# 三角函数
print(math.degrees(0.5 * math.pi))
print(math.radians(180 / math.pi))
print(math.sin(math.pi))
print(math.asin(math.sin(math.pi)))

